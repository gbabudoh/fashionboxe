const FREQUENT_COUNTRIES = [
  { code: 'US', name: 'United States', flag: '🇺🇸' },
  { code: 'FR', name: 'France', flag: '🇫🇷' },
  { code: 'IT', name: 'Italy', flag: '🇮🇹' },
  { code: 'GB', name: 'United Kingdom', flag: '🇬🇧' },
  { code: 'JP', name: 'Japan', flag: '🇯🇵' },
  { code: 'NG', name: 'Nigeria', flag: '🇳🇬' },
  { code: 'Global', name: 'Global', flag: '🌍' },
];

interface CountrySelectorProps {
  value?: string;
  onChange: (value: string) => void;
  className?: string;
  variant?: 'modal' | 'dropdown';
}

const CountrySelector = ({ value, onChange, className, variant = 'dropdown' }: CountrySelectorProps) => {
  const [open, setOpen] = useState(false);
  const [search, setSearch] = useState('');
  const wrapperRef = useRef<HTMLDivElement>(null);

  const selectedCountry = COUNTRIES.find((c) => c.name === value) || COUNTRIES.find((c) => c.name === 'Global');

  useEffect(() => {
    const handleClickOutside = (event: MouseEvent) => {
      if (wrapperRef.current && !wrapperRef.current.contains(event.target as Node)) {
        setOpen(false);
      }
    };
    document.addEventListener('mousedown', handleClickOutside);
    return () => document.removeEventListener('mousedown', handleClickOutside);
  }, []);

  const filteredCountries = COUNTRIES.filter((c) => 
    c.name.toLowerCase().includes(search.toLowerCase())
  );

  return (
    <div className={cn("relative", className)} ref={wrapperRef}>
      <button
        type="button"
        onClick={() => setOpen(!open)}
        className={cn(
          "flex items-center gap-3 rounded-full transition-all active:scale-95 cursor-pointer",
          variant === 'modal' 
            ? "bg-white/5 border border-white/10 px-6 py-3 hover:bg-white/10" 
            : "bg-accent/10 hover:bg-accent/20 px-4 py-2 text-accent"
        )}
      >
        <span className="text-xl">{selectedCountry?.flag}</span>
        <span className="text-xs font-bold uppercase tracking-widest">
          {selectedCountry?.name || "Global Mall"}
        </span>
        <ChevronsUpDown size={14} className="opacity-40" />
      </button>

      <AnimatePresence>
        {open && (
          <motion.div
            initial={{ opacity: 0, y: 10, scale: 0.95 }}
            animate={{ opacity: 1, y: 0, scale: 1 }}
            exit={{ opacity: 0, y: 10, scale: 0.95 }}
            className="absolute right-0 top-full z-50 mt-4 w-[320px] overflow-hidden rounded-[2rem] border border-white/10 bg-zinc-900/95 backdrop-blur-2xl shadow-[0_32px_64px_-16px_rgba(0,0,0,0.6)]"
          >
            {/* Search Header */}
            <div className="border-b border-white/5 p-4">
              <input
                type="text"
                placeholder="Find your region..."
                value={search}
                onChange={(e) => setSearch(e.target.value)}
                className="w-full rounded-2xl border border-white/5 bg-white/5 px-4 py-3 text-sm text-white placeholder:text-white/20 focus:outline-none focus:ring-1 focus:ring-accent/40"
                autoFocus
              />
            </div>

            <div className="max-h-[400px] overflow-y-auto p-4 scrollbar-hide">
              {!search && (
                <div className="mb-6">
                  <h3 className="mb-3 px-2 text-[10px] font-black uppercase tracking-[0.2em] text-white/30">Frequent Regions</h3>
                  <div className="grid grid-cols-2 gap-2">
                    {FREQUENT_COUNTRIES.map((country) => (
                      <button
                        key={country.code}
                        onClick={() => {
                          onChange(country.name);
                          setOpen(false);
                        }}
                        className={cn(
                          "flex items-center gap-2 rounded-xl border border-white/5 p-2 text-left transition-all hover:bg-white/5",
                          value === country.name ? "bg-accent/10 border-accent/20" : ""
                        )}
                      >
                        <span className="text-lg">{country.flag}</span>
                        <span className="text-[10px] font-bold text-white/80">{country.name}</span>
                      </button>
                    ))}
                  </div>
                </div>
              )}

              <h3 className="mb-3 px-2 text-[10px] font-black uppercase tracking-[0.2em] text-white/30">
                {search ? "Search Results" : "All Countries"}
              </h3>
              
              <div className="space-y-1">
                {filteredCountries.map((country) => (
                  <button
                    key={country.code}
                    onClick={() => {
                      onChange(country.name);
                      setOpen(false);
                      setSearch('');
                    }}
                    className={cn(
                      "flex w-full items-center justify-between rounded-xl px-4 py-2.5 text-sm transition-colors",
                      value === country.name ? "bg-accent/10 text-accent" : "text-white/60 hover:bg-white/5 hover:text-white"
                    )}
                  >
                    <div className="flex items-center gap-3">
                      <span className="text-xl">{country.flag}</span>
                      <span className="font-medium">{country.name}</span>
                    </div>
                    {value === country.name && <Check size={14} />}
                  </button>
                ))}
              </div>

              {filteredCountries.length === 0 && (
                 <div className="py-10 text-center text-xs text-white/20">
                    No regions match your search.
                 </div>
              )}
            </div>
            
            <div className="bg-white/5 p-4 text-center">
               <p className="text-[9px] font-bold uppercase tracking-widest text-white/30 italic">Global Runway Access Enabled</p>
            </div>
          </motion.div>
        )}
      </AnimatePresence>
    </div>
  );
};

export default CountrySelector;
